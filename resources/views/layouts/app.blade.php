    <script>
        

        $("#save-rename-wallet").on("click", function(){
            $("#rename-wallet-form").submit();
        });

        $(".rename-wallet").on("click", function(){
            $("#new-name").val($(this).data('name'));
            $("#rename-id").val($(this).data('id'));
        });

        $("#rename-wallet-form").on("submit", function(e){
            e.preventDefault();
            $("#error-body-rename").removeClass("d-block");
            var url = "{{ route('wallet.rename', ':id') }}";
            url = url.replace(':id', $("#rename-id").val());
            console.log(url);
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            $.ajax({
                url: url,
                type: "post",
                dataType: "json",
                data: {
                    "name": $('#new-name').val(),
                },
                success: function (data) {
                    location.reload();
                },
                error: function (data) {
                    
                    var errors = "";
                    $.each(data.responseJSON.errors, function(index, item) {
                        errors += "<li>" + item + "</li>";
                    });
                    $("#error-fields-rename").html(errors);
                    $("#error-body-rename").addClass("d-block");
                }
            });
        });
    </script>
